// Generates the "## Changes" section content for a GitHub Release by
// summarizing commit messages since the previous release with Z.ai.
// Writes the result to dist/changes.md (empty file on failure — release
// notes are best-effort and must never fail the release).
//
// Required environment: TAG, GITHUB_REPOSITORY, GH_TOKEN, ZAI_API_KEY.
// Optional: RELEASE_MODEL (defaults to glm-5.1).
import { execFileSync } from "node:child_process";
import { mkdirSync, writeFileSync } from "node:fs";

const apiKey = process.env.ZAI_API_KEY;
const model = process.env.RELEASE_MODEL || "glm-5.1";
const repo = process.env.GITHUB_REPOSITORY;
const currentTag = process.env.TAG;

const run = (command, args) => execFileSync(command, args, {
  encoding: "utf8",
  stdio: ["ignore", "pipe", "pipe"],
}).trim();

const exists = (ref) => {
  try {
    execFileSync("git", ["rev-parse", "--verify", "--quiet", ref], {
      stdio: "ignore",
    });
    return true;
  } catch {
    return false;
  }
};

const isAncestor = (ref) => {
  try {
    execFileSync("git", ["merge-base", "--is-ancestor", ref, currentTag], {
      stdio: "ignore",
    });
    return true;
  } catch {
    return false;
  }
};

const writeChanges = (notes) => {
  mkdirSync("dist", { recursive: true });
  writeFileSync("dist/changes.md", notes ? `${notes}\n` : "");
};

try {
  if (!apiKey) throw new Error("ZAI_API_KEY is required");
  if (!repo) throw new Error("GITHUB_REPOSITORY is required");
  if (!currentTag) throw new Error("TAG is required");

  execFileSync("git", ["fetch", "--force", "--tags"], { stdio: "inherit" });

  let previousTag = "";
  try {
    const releases = JSON.parse(run("gh", [
      "release",
      "list",
      "--repo",
      repo,
      "--exclude-drafts",
      "--limit",
      "100",
      "--json",
      "tagName",
    ]));
    previousTag = releases
      .map((release) => release.tagName)
      .find((tag) => tag !== currentTag && exists(`refs/tags/${tag}`) && isAncestor(tag)) ?? "";
  } catch (error) {
    console.error(`Failed to find previous GitHub release: ${error.message}`);
  }

  if (!previousTag) {
    previousTag = run("git", ["tag", "--merged", currentTag, "--sort=-creatordate", "--list", "v*"])
      .split("\n")
      .find((tag) => tag && tag !== currentTag) ?? "";
  }

  const range = previousTag ? `${previousTag}..${currentTag}` : currentTag;
  const commitMessages = run("git", ["log", "--format=%B%n---END COMMIT---", range]);
  const previousRelease = previousTag || "the beginning of the repository";

  const response = await fetch("https://api.z.ai/api/coding/paas/v4/chat/completions", {
    method: "POST",
    headers: {
      "Authorization": `Bearer ${apiKey}`,
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      model,
      temperature: 0.2,
      max_tokens: 2000,
      messages: [
        {
          role: "system",
          content: "You write concise GitHub release notes from git commit messages. Return only Markdown. Use level 4 headings only for non-empty sections named Features, Improvements, and Bugfixes. Each bullet must be one brief sentence in the form '- Short change - extremely short description'. Mention only important user-visible changes. Omit chores, tests, docs, refactors, and CI unless they materially affect users. Do not include code fences, introductions, conclusions, or empty sections. If there are no important changes, return an empty string.",
        },
        {
          role: "user",
          content: `Summarize important changes in ${currentTag} since ${previousRelease}.\n\nCommit messages:\n${commitMessages}`,
        },
      ],
    }),
  });

  if (!response.ok) {
    throw new Error(`Z.ai release notes request failed with ${response.status}: ${await response.text()}`);
  }

  const payload = await response.json();
  const notes = (payload.choices?.[0]?.message?.content ?? "")
    .replace(/^```(?:markdown)?\s*/i, "")
    .replace(/\s*```$/i, "")
    .trim();
  const generatedNote = `Release notes generated by ${model}.`;
  writeChanges(notes ? `${notes}\n\n${generatedNote}` : generatedNote);
} catch (error) {
  // Release notes are best-effort: never fail the release over them.
  console.error(`::warning::Failed to generate release notes: ${error.message}`);
  writeChanges("");
}
